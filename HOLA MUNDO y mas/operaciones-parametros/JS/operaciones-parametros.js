function  resta(numeroUno, numeroDos) {
    return numeroUno - numeroDos;
}

function multiplicacion(numeroUno, numeroDos) {
    return numeroUno * numeroDos;
}

function division(numeroUno, numeroDos) {
    return numeroUno / numeroDos;
}

console.log("El resultado de la resta es de :"+ resta(2, 2));
console.log("El resultado de la multiplicacion es de :"+multiplicacion(2, 2));
console.log("El resultado de la division es de :"+division(2, 2));
